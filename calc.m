syms x y z
x = input("enter number one")
y = input("enter number 2")
z = char(input("enter operation"))
if z == "+"
    disp(x+y)
elseif z == "-"
    disp(x-y)
elseif z == "*"
    disp(x*y)
elseif z == "/"
    disp(x/y)
end
